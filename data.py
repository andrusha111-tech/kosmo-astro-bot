def normalize_text(text):
    """Нормализует текст для поиска"""
    return text.strip().lower()

def get_astro_text(planet_name, zodiac_name, info_type=None):
    """
    Получить текст астрологического описания
    
    Args:
        planet_name (str): Название планеты на русском
        zodiac_name (str): Название знака зодиака на русском
        info_type (str, optional): Тип информации (для Венеры и Марса)
    
    Returns:
        str: Текст описания или сообщение об ошибке
    """
    try:
        # Нормализуем входные данные
        planet_norm = normalize_text(planet_name)
        zodiac_norm = normalize_text(zodiac_name)
        
        # Преобразуем в ключи
        planet_key = PLANET_NAMES.get(planet_norm)
        zodiac_key = ZODIAC_NAMES.get(zodiac_norm)
        
        if not planet_key:
            return f"❌ Планета '{planet_name}' не найдена."
        if not zodiac_key:
            return f"❌ Знак зодиака '{zodiac_name}' не найден."
        
        # Для Венеры и Марса нужно выбрать тип информации
        if planet_key in ["ВЕНЕРА", "МАРС"]:
            if not info_type:
                # Если тип не указан, возвращаем список доступных типов
                types_list = PLANET_INFO_TYPES.get(planet_key, [])
                types_text = "\n".join([f"• {t.replace('_', ' ')}" for t in types_list])
                return f"""Для {planet_name} доступны следующие типы информации:

{types_text}

Пожалуйста, уточните запрос, например: "{planet_name} в {zodiac_name} удовольствие" """
            
            # Проверяем, существует ли такой тип для планеты
            if info_type not in PLANET_INFO_TYPES.get(planet_key, []):
                available_types = ", ".join(PLANET_INFO_TYPES.get(planet_key, []))
                return f"❌ Для {planet_name} тип '{info_type}' не найден. Доступные типы: {available_types}"
            
            # Получаем текст
            if (planet_key in astro_data and 
                zodiac_key in astro_data[planet_key] and 
                info_type in astro_data[planet_key][zodiac_key]):
                return astro_data[planet_key][zodiac_key][info_type]
            else:
                return f"⚠️ Информация для {planet_name} в {zodiac_name} (тип: {info_type}) пока не добавлена в базу."
        
        else:
            # Для остальных планет - обычный доступ
            if planet_key in astro_data and zodiac_key in astro_data[planet_key]:
                return astro_data[planet_key][zodiac_key]
            else:
                return f"⚠️ Информация для {planet_name} в {zodiac_name} пока не добавлена в базу."
            
    except Exception as e:
        return f"⛔ Ошибка: {str(e)}"

